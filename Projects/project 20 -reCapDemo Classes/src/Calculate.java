public class Calculate {
    public int gather(int num1 , int num2){
        return num1+num2;
    }

    public int extraction(int num1 , int num2){
        return num1-num2;
    }

    public int multiplication(int num1 , int num2){
        return num1*num2;
    }

    public int divide(int num1 , int num2){
        return num1/num2;
    }
}
